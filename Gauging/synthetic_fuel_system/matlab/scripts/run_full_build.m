function mdl = createModel(name, model_dir, dd_path)
% CREATEMODEL  Create a new Simulink model or reset an existing one.
%
%   mdl = createModel(name, model_dir, dd_path)
%
%   Creates a new model named 'name', deleting any existing .slx file
%   first. Configures the model to:
%     - Use ode23tb solver (stiff-capable, appropriate for hybrid systems)
%     - Run for 1000 seconds (covers full defuel sequence)
%     - Max step size 0.1s (10 Hz update rate, matches probe sample rate)
%     - Link to the data dictionary for all parameter resolution
%     - Return outputs to the MATLAB workspace as timeseries
    mdl = name;
    full_path = fullfile(model_dir, [name '.slx']);
    if bdIsLoaded(mdl), close_system(mdl, 0); end
    if exist(full_path, 'file'), delete(full_path); end

    new_system(mdl);
    % DataDictionary takes filename only (must be on MATLAB path)
    [~, dd_name, dd_ext] = fileparts(dd_path);
    set_param(mdl, 'DataDictionary', [dd_name dd_ext]);
    set_param(mdl, 'Solver', 'ode23tb');
    set_param(mdl, 'StopTime', '1000');
    set_param(mdl, 'MaxStep', '0.1');
    set_param(mdl, 'SaveTime', 'on');
    set_param(mdl, 'SaveOutput', 'on');
    set_param(mdl, 'ReturnWorkspaceOutputs', 'on');
end

function saveModel(mdl, model_dir)
% SAVEMODEL  Save a model to the models/ directory and close it.
    save_system(mdl, fullfile(model_dir, [mdl '.slx']));
    close_system(mdl, 0);
    fprintf('  Saved: %s.slx\n', mdl);
end

function addOrUpdateDD(section, name, value)
% ADDORUPDATEDD  Add or update an entry in a data dictionary section.
%   If the entry already exists, its value is updated in place.
%   If it doesn't exist, a new entry is created.
    try
        entry = getEntry(section, name);
        setValue(entry, value);
    catch
        addEntry(section, name, value);
    end
end

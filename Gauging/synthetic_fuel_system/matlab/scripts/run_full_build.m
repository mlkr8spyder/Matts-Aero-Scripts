%% run_full_build.m
% Master script: runs the complete Simulink model build pipeline.
%
% OVERVIEW
%   This script builds a complete Simulink model for a 5-tank Fuel Quantity
%   Indication System (FQIS) with capacitance probes, probe failure detection,
%   3-D height-volume lookup tables, dielectric-to-density conversion, and a
%   single-point refuel system with high-level shutoff valves.
%
% PIPELINE
%   Step 1: Project setup — create directories and an empty data dictionary
%   Step 2: Populate data dictionary — buses, parameters, tank structs, 3-D LUTs
%   Step 3: Build Simulink models — 12 .slx files with model references
%   Step 4: Validate — 50 automated checks (file existence, dict entries, LUT
%           dimensions, monotonicity, and model loadability)
%
% ARCHITECTURE
%   The top-level model (FuelGaugingSystem.slx) references sub-models for each
%   functional block. All parameters come from the data dictionary — nothing
%   is hard-coded in the base workspace. The per-tank processing chain is:
%
%     FuelWL --> ProbeModel_{Real|Combo|Pseudo}   Convert fuel surface to probe height
%           --> ProbeFailureDetector               Open/short/rate fault detection + LKG hold
%           --> HV_TableLookup_T{1-5}              3-D n-D interpolation (height x pitch x roll)
%           --> Gain (1/231)                       Cubic inches to US gallons
%           --> Product (gallons x density)         Final weight per tank
%
%   Density is computed once from the dielectric constant:
%     Dielectric --> DensityComputation --> density (shared across all tanks)
%
%   The refuel system monitors fuel waterlines and controls shutoff valves:
%     FuelWL_T{1-5} + Refuel_Cmd --> RefuelSystem --> valve/sensor status
%
% DESIGN DECISIONS
%   - Per-tank HV lookup models: Simulink n-D Lookup Table objects cannot be
%     parameterized by name at runtime, so each tank gets its own .slx file
%     (HV_TableLookup_T1 through T5) wired to its specific LUT object.
%
%   - Model workspace variables (not model arguments): The ParameterArgumentNames
%     API requires variables to already exist in the model workspace before they
%     can be declared as arguments. We use assignin(mdlWorkspace, ...) to set
%     default values that can be overridden by the parent model.
%
%   - Homogeneous TankParams struct: MATLAB struct arrays require all elements
%     to have the same fields. Tanks without combo probes use NaN for the
%     lower_base/lower_top/upper_base/upper_top fields.
%
%   - Data dictionary filename: set_param(..., 'DataDictionary') takes just the
%     filename (e.g., 'FuelGaugingData.sldd'), not the full path. The directory
%     containing the .sldd must be on the MATLAB path.
%
% PREREQUISITES
%   - MATLAB R2024b+ with Simulink
%   - H-V table data in data/tank_system.mat (generated by Python pipeline or
%     generate_hv_tables.m). This file contains cell arrays of height and volume
%     vectors for all 5 tanks across 13 pitch x 17 roll attitude combinations.
%
% USAGE
%   >> cd synthetic_fuel_system/matlab/scripts
%   >> run_full_build
%
% See also: build_data_dictionary.m, build_simulink_model.m, validate_model.m


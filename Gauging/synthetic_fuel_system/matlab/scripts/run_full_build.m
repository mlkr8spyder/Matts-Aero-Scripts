% --- 3a. ProbeModel_Real ---
% Models a single capacitance probe (used for T1, T2, T4).
%
% Signal flow:
%   fuel_wl (WL at probe, inches)
%     --> Subtract probe_base_wl --> raw height above probe base
%     --> Clamp to [0, active_length] --> physical wetted height
%     --> Add band-limited white noise (sigma=0.02", Cov=0.0004)
%     --> probe_height (output, inches)
%
%   fuel_wl also passes through directly to fuel_wl_at_probe (for logging)
%
% The "Saturation Dynamic" block clamps the probe reading between zero
% (probe fully dry) and the active length (probe fully submerged).
% Real probes cannot report negative heights or heights beyond their length.
%
% Model workspace defaults are for T1 (FS 210). When instantiated in the
% top-level model for T2 or T4, these values would be overridden.

% ---------- 2d. Lookup Table Objects ----------
% Load the Python-generated H-V tables and convert them into Simulink
% LookupTable objects for use by the n-D Lookup Table blocks.
%
% The source .mat file contains cell arrays where each cell holds a
% variable-length vector (different heights have slightly different
% point counts at extreme attitudes). We must:
%   1. Pick a common height breakpoint grid (from the level-attitude table)
%   2. Interpolate all attitude tables onto this common grid
%   3. Enforce monotonicity (volume must not decrease with height)
%   4. Clamp negative volumes to zero
%   5. Package into a Simulink.LookupTable with 3 breakpoint dimensions
%
% The resulting LUT objects are stored in the data dictionary and
% referenced by name from the HV_TableLookup_T{n}.slx models.
fprintf('\n--- Creating Lookup Table Objects ---\n');

hv_file = fullfile(data_dir, 'tank_system.mat');
if ~exist(hv_file, 'file')
    error('H-V table data not found: %s\nRun the Python pipeline first.', hv_file);
end

hv_data = load(hv_file);
fprintf('  Loaded H-V data from: %s\n', hv_file);

pitch_bp = hv_data.pitch_range(:)';
roll_bp  = hv_data.roll_range(:)';

for t = 1:5
    prefix = sprintf('T%d', t);
    heights_cell = hv_data.([prefix '_heights']);
    volumes_cell = hv_data.([prefix '_volumes']);

    n_pitch = numel(pitch_bp);
    n_roll  = numel(roll_bp);

    % Use level-attitude table as reference height grid
    pi0 = find(pitch_bp == 0);
    ri0 = find(roll_bp == 0);
    ref_heights = heights_cell{pi0, ri0};
    ref_heights = ref_heights(:)';
    n_h = numel(ref_heights);

    % Build 3-D table array: (n_h x n_pitch x n_roll)
    table_3d = zeros(n_h, n_pitch, n_roll);

    for pi = 1:n_pitch
        for ri = 1:n_roll
            h_raw = heights_cell{pi, ri}; h_raw = h_raw(:)';
            v_raw = volumes_cell{pi, ri}; v_raw = v_raw(:)';

            if numel(h_raw) == n_h && all(abs(h_raw - ref_heights) < 0.01)
                table_3d(:, pi, ri) = v_raw;
            else
                table_3d(:, pi, ri) = interp1(h_raw, v_raw, ref_heights, ...
                    'linear', 'extrap');
            end
        end
    end

    % Enforce monotonicity
    for pi = 1:n_pitch
        for ri = 1:n_roll
            col = table_3d(:, pi, ri);
            for k = 2:n_h
                if col(k) < col(k-1)
                    col(k) = col(k-1);
                end
            end
            table_3d(:, pi, ri) = col;
        end
    end
    table_3d = max(0, table_3d);

    lut = Simulink.LookupTable;
    lut.Table.Value = table_3d;
    lut.Table.DataType = 'double';
    lut.Breakpoints(1).Value = ref_heights;
    lut.Breakpoints(1).DataType = 'double';
    lut.Breakpoints(2).Value = pitch_bp;
    lut.Breakpoints(2).DataType = 'double';
    lut.Breakpoints(3).Value = roll_bp;
    lut.Breakpoints(3).DataType = 'double';
    lut.StructTypeInfo.Name = sprintf('LUT_%s_Type', prefix);

    lut_name = sprintf('LUT_%s', prefix);
    addOrUpdateDD(ddata, lut_name, lut);

    fprintf('  %s: %dx%dx%d = %d entries (height: [%.1f, %.1f])\n', ...
        lut_name, n_h, n_pitch, n_roll, numel(table_3d), ...
        ref_heights(1), ref_heights(end));
end


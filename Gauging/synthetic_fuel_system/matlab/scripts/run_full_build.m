            'autorouting', 'smart');
    end

    % Failure detector
    add_block('simulink/Ports & Subsystems/Model', [mdl '/' prefix '_Failure'], ...
        'ModelName', 'ProbeFailureDetector', ...
        'Position', [380 y 480 y+50]);

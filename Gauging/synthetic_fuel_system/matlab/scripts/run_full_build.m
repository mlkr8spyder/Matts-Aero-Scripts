%
% OUTPUTS:
%   TotalWeight --> To Workspace ('total_fuel_weight' timeseries)
%   TotalWeight --> Scope (for interactive viewing)
%
% T5 NOTE: In the top-level harness, T5's pseudo probe receives FuelWL_T5

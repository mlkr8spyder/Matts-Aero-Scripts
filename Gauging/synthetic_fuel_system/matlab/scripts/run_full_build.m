    add_block('simulink/Sinks/Out1', [mdl '/' prefix '_valve_open'], ...
        'Position', [370 y 400 y+20], 'Port', sprintf('%d', 2*t-1));
    add_block('simulink/Sinks/Out1', [mdl '/' prefix '_hi_level'], ...
